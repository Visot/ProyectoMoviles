    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).

            switch (position) {
                case 0:
                    TabCentros tab1 = new TabCentros();
                    return tab1;
                case 1:
                    TabOficinas tab2 = new TabOficinas();
                    return tab2;
                case 2:
                    TabFacultades tab3 = new TabFacultades();
                    return tab3;
                default:
                    return null;
            }

//            TabCentros tab1 = new TabCentros();
//            return tab1;
//            return PlaceholderFragment.newInstance(position + 1);
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return getString(R.string.centros_Inv);
                case 1:
                    return getString(R.string.oficinas);
                case 2:
                    return getString(R.string.facultades);
            }
            return null;
        }
    }


